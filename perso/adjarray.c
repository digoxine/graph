#include <stdlib.h>
#include <stdio.h>
#include <time.h>//to estimate the runing time

#define NLINKS 100000 //maximum number of edges for memory allocation, will increase if needed

//Notre structure des sommets visites initialisé à 0
typedef struct {
  unsigned long nbNoeudsvisites;
  unsigned int taille;
  unsigned long *visited;
}Visited;

typedef struct {
  unsigned long debut;
  unsigned long fin;
  unsigned long *fifo;
} FIFO;
typedef struct {
	unsigned long s;
	unsigned long t;
} edge;

//edge list structure:
typedef struct {
	unsigned long n;//number of nodes
	unsigned long e;//number of edges
	edge *edges;//list of edges
	unsigned long *cd;//cumulative degree cd[0]=0 length=n+1
	unsigned long *adj;//concatenated lists of neighbors of all nodes
} adjlist;

//compute the maximum of three unsigned long
inline unsigned long max3(unsigned long a,unsigned long b,unsigned long c){
	a=(a>b) ? a : b;
	return (a>c) ? a : c;
}

//reading the edgelist from file
adjlist* readedgelist(char* input){
	unsigned long e1=NLINKS;
	FILE *file=fopen(input,"r");

	adjlist *g=malloc(sizeof(adjlist));
	g->n=0;
	g->e=0;
	g->edges=malloc(e1*sizeof(edge));//allocate some RAM to store edges
	printf("Avant le while\n");
	while (fscanf(file,"%lu %lu", &(g->edges[g->e].s), &(g->edges[g->e].t))==2) {
		g->n=max3(g->n,g->edges[g->e].s,g->edges[g->e].t);
		if (++(g->e)==e1) {//increase allocated RAM if needed
			e1+=NLINKS;
			g->edges=realloc(g->edges,e1*sizeof(edge));
		}
	}
	fclose(file);

	g->n++;

	g->edges=realloc(g->edges,g->e*sizeof(edge));

	return g;
}

//building the adjacency matrix
void mkadjlist(adjlist* g){
	unsigned long i,u,v;
	unsigned long *d=calloc(g->n,sizeof(unsigned long));

	for (i=0;i<g->e;i++) {
		d[g->edges[i].s]++;
		d[g->edges[i].t]++;
	}

	g->cd=malloc((g->n+1)*sizeof(unsigned long));
	g->cd[0]=0;
	for (i=1;i<g->n+1;i++) {
		g->cd[i]=g->cd[i-1]+d[i-1];
		d[i-1]=0;
	}
	//Non oriente justifie 2. Car tous les voisins de u contiennent v et tous les voisins de v contiennent u
	g->adj=malloc(2*g->e*sizeof(unsigned long));

	for (i=0;i<g->e;i++) {
		u=g->edges[i].s;
		v=g->edges[i].t;
		g->adj[ g->cd[u] + d[u]++ ]=v;
		g->adj[ g->cd[v] + d[v]++ ]=u;
	}

	free(d);
	//free(g->edges);
}


//freeing memory
void free_adjlist(adjlist *g){
  free(g->edges);
  free(g->cd);
  free(g->adj);
  free(g);
}

void markNode(Visited *v, unsigned long s )
{
  v->nbNoeudsvisites = ++v->nbNoeudsvisites;
  v->visited[s] = 1;
}

//Renvoie le premier noeud qui n'a pas été visité
int FirstNodeNotVisited(Visited *visit){
  for(int i = 0 ; i<visit->taille; i++)
    {
      if(visit->visited[i] ==0)
	return i;
    }
  return -1;
}

int allVisited(Visited *visit){
  if(visit->nbNoeudsvisites == visit->taille){
    return 0;
  }
  else
    return 1;      
}
 




